                        <h1><b>Izvor hranjivih tvari</b></h1>
                        <br>
                        <p style="text-align: justify; p-2">Dodaci prehrani su koncentrirani izvor hranjivih ili drugih tvari s prehrambenim ili fiziološkim funkcijama, sami ili u kombinacijama. Svrha im je potpomaganje unosa hranjivih tvari i nadopuna prehrane tvarima koje se normalnim unosom hrane u organizam ne dobivaju u dovoljnoj količini, a sve u svrhu povoljna učinka na zdravlje. Na taj način povećava se opća otpornost organizma na stresne vanjske utjecaje, pomaže u održavanju tjelesnih fizioloških funkcija.

Hranjive i druge tvari koje čine dodatke prehrani su: vitamini i minerali, bjelančevine, aminokiseline, masne kiseline, celuloza, pivski kvasac, inulin, lecitin, glukomanan, bilje i ljekovito bilje te njihovi ekstrakti, koncentrati ili sirovine biljnog podrijetla, zaštitna sredstva biljnog podrijetla (bioflavonoidi, karotenoidi, izoflavoni, glukozinolati), prirodni enzimi i koenzimi, žive kulture mikroorganizama, organska tkiva, metaboliti žlijezda i druge tvari. Dodaci prehrani su i pčelinji proizvodi i njihove mješavine.

Za proizvodnju biljnih pripravaka kao dodataka prehrani mogu se koristiti svježe ili osušene cijele biljke te njihovi dijelovi (npr. list, korijen, plod, cvijet i dr.), čiste tvari biljnog podrijetla pojedinačno ili u smjesi, biljni sekundarni proizvodi. Obvezno je navesti hrvatsko i latinsko ime ljekovite biljne vrste i biljni organ.

Dodaci prehrani plasiraju se na tržište u različitim doziranim oblicima (tablete, kapsule, prah, tekućina, bar pločice, čaj, kapi, sirup i slično), a na deklaraciji moraju nositi oznaku "dodatak prehrani".</p>
